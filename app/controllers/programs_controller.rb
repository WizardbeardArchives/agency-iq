class ProgramsController < ApplicationController
  before_action :set_program, only: [:show, :edit, :update, :destroy]

  # GET /programs
  # GET /programs.json
  def index
    @programs = Program.all
  end

  # GET /programs/1
  # GET /programs/1.json
  def show
  end

  # GET /programs/new
  def new
    @program = Program.new
  end

  # GET /programs/1/edit
  def edit
  end

  # POST /programs
  # POST /programs.json
  def create
    @program = Program.new(program_params)

    respond_to do |format|
      if @program.save
        format.html { redirect_to @program, notice: 'Program was successfully created.' }
        format.json { render action: 'show', status: :created, location: @program }
      else
        format.html { render action: 'new' }
        format.json { render json: @program.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /programs/1
  # PATCH/PUT /programs/1.json
  def update
    respond_to do |format|
      if @program.update(program_params)
        format.html { redirect_to @program, notice: 'Program was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: 'edit' }
        format.json { render json: @program.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /programs/1
  # DELETE /programs/1.json
  def destroy
    @program.destroy
    respond_to do |format|
      format.html { redirect_to programs_url }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_program
      @program = Program.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def program_params
      params.require(:program).permit(:primaryKey, :PGM_CODE, :PGM_TIME_PGM, :PGM_RATING_EA, :PGM_COST_EA, :PGM_GROUP, :PGM_NOTE, :PGM_TYPE, :AD_NAME, :AD_POSITION, :AD_NOTES, :AD_GROSS_AMT, :AD_COMM, :AD_NET_AMT, :AD_EXTRA_AMT, :AD_BILL_NET, :AD_ISSUE_DATE, :AD_SPACE_CLOSE, :AD_MAT_CLOSE, :PGM_KEY, :PGM_CLIENT_NUM, :AD_RATE_BASIS, :PGM_ADDED_BY, :PGM_ADDED_WHEN, :PGM_EDITED_BY, :PGM_EDITED_WHEN)
    end
end
